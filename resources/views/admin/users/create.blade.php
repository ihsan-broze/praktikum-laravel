<script>
// Toggle Password Visibility
document.getElementById('togglePassword').addEventListener('click', function() {
    const password = document.getElementById('password');
    const icon = this.querySelector('i');
    
    if (password.type === 'password') {
        password.type = 'text';
        icon.classList.remove('fa-eye');
        icon.classList.add('fa-eye-slash');
    } else {
        password.type = 'password';
        icon.classList.remove('fa-eye-slash');
        icon.classList.add('fa-eye');
    }
});

document.getElementById('togglePasswordConfirm').addEventListener('click', function() {
    const password = document.getElementById('password_confirmation');
    const icon = this.querySelector('i');
    
    if (password.type === 'password') {
        password.type = 'text';
        icon.classList.remove('fa-eye');
        icon.classList.add('fa-eye-slash');
    } else {
        password.type = 'password';
        icon.classList.remove('fa-eye-slash');
        icon.classList.add('fa-eye');
    }
});

// Role Description
const roleDescriptions = {
    'admin': 'Full access to all system features and user management.',
    'manager': 'Can manage team members and access management reports.',
    'employee': 'Standard user with access to assigned tasks and features.',
    'user': 'Basic user with limited access to core features.'
};

document.getElementById('role').addEventListener('change', function() {
    const roleDescription = document.getElementById('roleDescription');
    const roleDescriptionText = document.getElementById('roleDescriptionText');
    
    if (this.value && roleDescriptions[this.value]) {
        roleDescriptionText.textContent = roleDescriptions[this.value];
        roleDescription.style.display = 'block';
    } else {
        roleDescription.style.display = 'none';
    }
});

// Generate Password
function generatePassword() {
    const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789!@#$%^&*';
    let password = '';
    for (let i = 0; i < 12; i++) {
        password += chars.charAt(Math.floor(Math.random() * chars.length));
    }
    document.getElementById('password').value = password;
    document.getElementById('password_confirmation').value = password;
}

// Add generate password button
document.addEventListener('DOMContentLoaded', function() {
    const passwordGroup = document.querySelector('#password').closest('.input-group');
    const generateBtn = document.createElement('button');
    generateBtn.className = 'btn btn-outline-info';
    generateBtn.type = 'button';
    generateBtn.innerHTML = '<i class="fas fa-key"></i>';
    generateBtn.title = 'Generate Password';
    generateBtn.onclick = generatePassword;
    
    passwordGroup.appendChild(generateBtn);
});
</script>
